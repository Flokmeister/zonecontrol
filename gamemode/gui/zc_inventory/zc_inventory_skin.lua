function SKIN:PaintButton(pnl, w, h)
	if pnl.ShortButton then
		kingston.gui.FindFunc(pnl, "Paint", "ShortButton", w, h)
		return
	end
	
	if pnl.LongButton then
		kingston.gui.FindFunc(pnl, "Paint", "LongButton", w, h)
		return
	end
end

